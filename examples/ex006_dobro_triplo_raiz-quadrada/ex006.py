# EXERCÍCIO: peça um valor para o usuário e mostra pra ele qual é o seu dobro, o triplo e a raiz quadrada dele!

number = int(input('Digite um número: '))

print(f'O dobro de {number} é {number*2}!')
print(f'O triplo de {number} é {number*3}!')
print(f'A raiz quadrada de {number} é aproximadamente {number**(1/2):.2f}!')
