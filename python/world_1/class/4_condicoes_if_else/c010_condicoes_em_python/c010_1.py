# Meia de notas:

note1 = float(input("Qual foi a primeira nota: "))
note2 = float(input("Qual foi a segunda nota? "))
average = (note1 + note2) / 2

if average >= 7.5:
    print('PARABÉNS! Você passou!')

else:
    print(f'Com a nota {average} você não passa para o próximo ano!')

