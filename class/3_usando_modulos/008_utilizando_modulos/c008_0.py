# Para importar uma biblioteca toda, eu uso IMPORT TARARA <= assim ele trará todas as funcionalidades dela;
# Para importar uma unica funcionalidade da biblioteca, nós usamos FROM TARARA IMPORT X <= Esse x é algo especifico. Ou seja, eu limito a importação a uma única funcionalidade ou a funcionalidades específicas, para que eu não importe um biblioteca inteira desnecessáriamente, já que eu não vou usar ela toda! (importação otimizada)

# Ex:
# math => biblioteca matemática

#       = ceil      => arredondar pra cima
#       = floor     => arredondar pra baixo
#       = trunc     => truncate, truncar = simplesmente elimina da virgula pra frente
#       = pow       => exponenciação, potência
#       = sqrt      => square root, raiz quadrada
#       = factorial => fatorial
