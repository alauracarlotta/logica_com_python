# Exemplos:

nome = input('Digite o seu nome: ')
print(f'1 Prazer em te conhecer {nome}!')

print(f'2 Prazer em te conhecer {nome*5}!') # Multiplica o nome por 5x

print(f'3 Prazer em te conhecer {nome:20}!') # Escreve em 20caracteres

print(f'4 Prazer em te conhecer {nome:>20}!') # Escreve em 20caracteres alinhando a direita

print(f'5 Prazer em te conhecer {nome:<20}!') # Escreve em 20caracteres alinhando a esquerda

print(f'6 Prazer em te conhecer {nome:^20}!') # Escreve em 20caracteres centralizando a palvra

print(f'7 Prazer em te conhecer {nome:=^20}!') # Escreve em 20caracteres centralizando a palvra e preenchenco os espaÃ§os com o sinal de =. (mas poderia ser qualque outro sinal)
